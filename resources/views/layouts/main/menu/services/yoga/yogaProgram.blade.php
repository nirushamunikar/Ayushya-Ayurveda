                                            <p>Precautions for senior while practicing Yoga:</p>
                                            <p>Here are some key things for elders to achieve good benefits from
                                                yoga. When people start yoga at age of 60 or older, they usually
                                                have various health issues commonly associated with the aging
                                                process so they have to take care of themselves.</p>
                                            <ul style="list-style-type:disc;">
                                                <li> A slow practice is easier for seniors and takes relaxation in
                                                    between every yoga asana.
                                                </li>
                                                <li> While practicing Yoga, you have to know your body and respect
                                                    its limits. Do not push yourself to hard yoga postures beyond
                                                    your capacity. Yoga can only be effective if you practice it
                                                    correctly according to your own capacity.
                                                </li>
                                                <li> Seniors may not have enough strength to hold the pose for a
                                                    longer period. So, observe yourself and make shorter (ten to
                                                    fifteen seconds).
                                                </li>
                                                <li> Focus your awareness on your stretching portion of the body and
                                                    gaze in a specific spot to assist with balance and peace of
                                                    mind.
                                                </li>
                                                <li> Support and protect joints like ankles, hips, hamstrings,
                                                    wrists and pectorals.
                                                </li>
                                                <li> Knowledge of modifications of Asanas will make everything
                                                    easier and will be more beneficial.
                                                </li>
                                                <li> Keep the Focus on functioning</li>
                                                <li> Please do not practice watching television, You tube, CD etc.
                                                    Please take help from a yoga expert.
                                                </li>
                                                <li> Yoga postures or stretches should never hurt; release the
